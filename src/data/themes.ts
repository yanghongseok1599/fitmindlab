/**
 * Lookup map: ThemeId -> Theme
 */
export const THEME_MAP: Record<ThemeId, Theme> = THEMES.reduce(
  (map, theme) => {
    map[theme.id] = theme;
    return map;
  },
  {} as Record<ThemeId, Theme>,
);

/**
 * Get a theme by its ID.
 * Throws an error if the theme is not found.
 */
export function getThemeById(id: ThemeId): Theme {
  const theme = THEME_MAP[id];
  if (!theme) {
    throw new Error(`Theme not found: ${id}`);
  }
  return theme;
}
