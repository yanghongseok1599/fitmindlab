// 시너지 분석을 위한 헬퍼 함수
export const getSynergyAnalysis = (strengthNames: string[]): { title: string; description: string }[] => {
  const synergies: { title: string; description: string }[] = [];

  for (let i = 0; i < strengthNames.length; i++) {
    for (let j = i + 1; j < strengthNames.length; j++) {
      const key1 = `${strengthNames[i]}+${strengthNames[j]}`;
      const key2 = `${strengthNames[j]}+${strengthNames[i]}`;

      if (STRENGTH_SYNERGIES[key1]) {
        synergies.push(STRENGTH_SYNERGIES[key1]);
      } else if (STRENGTH_SYNERGIES[key2]) {
        synergies.push(STRENGTH_SYNERGIES[key2]);
      }
    }
  }

  return synergies.slice(0, 3); // 최대 3개만 반환
};
